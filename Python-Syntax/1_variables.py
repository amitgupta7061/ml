# Variables and Data Types

# Integer
age = 25
print("Age:", age, "Type:", type(age))

# Float
height = 5.9
print("Height:", height, "Type:", type(height))

# String
name = "John Doe"
print("Name:", name, "Type:", type(name))

# Boolean
is_student = True
print("Is Student:", is_student, "Type:", type(is_student))

# Type Conversion
age_str = str(age)
print(f"Age as string: {age_str}")
